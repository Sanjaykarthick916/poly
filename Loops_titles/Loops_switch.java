package Loops_titles;

public class Loops_switch {
	public static void main(String[] args) {
		String cars = "audi";
		switch(cars) {
		
		case "BMW":
			System.out.println("730d");
			break;
		
		case "Range rover":
			System.out.println("velar");
			break;
			
		case "mercedes":
			System.out.println("E230d");
			
			
			default:
				System.out.println("no cars found");
		}	
		
	}
		
}


